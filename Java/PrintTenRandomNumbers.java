import java.util.Random;

public class PrintTenRandomNumbers {

    public static void main(String args[]){
        
        //create variables
        int i,s;
        
        //loop through to print
        for(i=1; i<=10; i++){
            s=(int)Math.random()*1000;
        }
        
        System.out.println(s);
        
        
    }

}